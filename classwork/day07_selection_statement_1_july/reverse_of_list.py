'''------- Problem Statement: Reverse a List

Write a Python program to create a list of numbers.
Reverse the list using the reverse() method.
Display the reversed list.
-------------------------------------------------------------'''

#------- Coding ---------------

#create a list
numbers = [30,90,70,50]

print("Numbers are :")
print(numbers)

print("--------------------------------------------")

#reverse the list
numbers.reverse()

print("After reversing the list :")
print(numbers)

#----------------------------------------------------
'''Output :
Numbers are :
[30, 90, 70, 50]
--------------------------------------------
After reversing the list :
[50, 70, 90, 30]
--------------------------------------------'''